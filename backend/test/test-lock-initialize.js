import axios from 'axios';
import md5 from 'md5';
import dotenv from 'dotenv';

// Load environment variables
dotenv.config();

const TTLOCK_CLIENT_ID = process.env.TTLOCK_CLIENT_ID;
const TTLOCK_CLIENT_SECRET = process.env.TTLOCK_CLIENT_SECRET;
const TTLOCK_API_BASE_URL = process.env.TTLOCK_API_BASE_URL || 'https://api.sciener.com';

console.log('🔐 Testing TTLock Lock Initialize API');
console.log('======================================');
console.log('');
console.log('⚠️  IMPORTANT NOTE:');
console.log('This API requires lockData generated by the TTLock SDK.');
console.log('The lockData is obtained when you physically add a lock using the SDK.');
console.log('');
console.log('This test will demonstrate the API call structure, but will likely fail');
console.log('without real lockData from an actual lock initialization via SDK.');
console.log('');
console.log('For a successful test, you need:');
console.log('1. TTLock SDK installed in your app');
console.log('2. Physical TTLock device');
console.log('3. lockData generated from SDK.addLock() method');
console.log('');
console.log('════════════════════════════════════════════════════════════════');
console.log('');

// Get credentials from command line or use defaults
const username = process.argv[2] || 'tusharvaishnavtv@gmail.com';
const password = process.argv[3] || 'Tushar@900';
const lockAlias = process.argv[4] || 'Test Lock';

// Placeholder lockData (this won't work for real initialization)
// In production, this comes from: SDK.addLock() callback
const lockData = process.argv[5] || 'PLACEHOLDER_LOCK_DATA_FROM_SDK';

console.log('Test Configuration:');
console.log('Username:', username);
console.log('Lock Alias:', lockAlias);
console.log('Lock Data:', lockData.substring(0, 50) + (lockData.length > 50 ? '...' : ''));
console.log('');

async function testLockInitialize() {
  try {
    // Step 1: Get access token
    console.log('Step 1: Getting access token...');
    const hashedPassword = md5(password).toLowerCase();

    const tokenResponse = await axios.post(
      `${TTLOCK_API_BASE_URL}/oauth2/token`,
      null,
      {
        params: {
          client_id: TTLOCK_CLIENT_ID,
          client_secret: TTLOCK_CLIENT_SECRET,
          username: username,
          password: hashedPassword,
          grant_type: 'password'
        }
      }
    );

    const accessToken = tokenResponse.data.access_token;
    console.log('✅ Access token obtained');
    console.log('   Token:', accessToken.substring(0, 20) + '...');
    console.log('');

    // Step 2: Initialize lock
    console.log('Step 2: Initializing lock...');
    console.log('');

    const params = {
      clientId: TTLOCK_CLIENT_ID,
      accessToken: accessToken,
      lockData: lockData,
      lockAlias: lockAlias,
      date: Date.now()
    };

    console.log('📡 Calling TTLock Lock Initialize API...');
    console.log('Endpoint:', `${TTLOCK_API_BASE_URL}/v3/lock/initialize`);
    console.log('');

    const response = await axios.post(
      `${TTLOCK_API_BASE_URL}/v3/lock/initialize`,
      null,
      { params }
    );

    // Check for errors in response
    if (response.data.errcode && response.data.errcode !== 0) {
      console.error('❌ Lock initialization failed');
      console.error('');
      console.error('📊 Response:');
      console.error(JSON.stringify(response.data, null, 2));
      console.error('');

      if (response.data.errcode === -3) {
        console.error('ℹ️  Error -3: Invalid Parameter');
        console.error('   This is expected when using placeholder lockData.');
        console.error('   Real lockData must come from TTLock SDK.addLock() method.');
        console.error('');
        console.error('To get real lockData:');
        console.error('1. Integrate TTLock SDK in your mobile app');
        console.error('2. Use SDK.addLock() method with a physical lock');
        console.error('3. The SDK will return lockData in the success callback');
        console.error('4. Pass that lockData to this API');
      }
      return;
    }

    console.log('✅ SUCCESS! Lock initialized successfully');
    console.log('');
    console.log('📊 Response:');
    console.log(JSON.stringify(response.data, null, 2));
    console.log('');

    if (response.data.lockId && response.data.keyId) {
      console.log('🎉 LOCK INITIALIZED:');
      console.log('   Lock ID:', response.data.lockId);
      console.log('   Admin Key ID:', response.data.keyId);
      console.log('');
      console.log('✅ You can now:');
      console.log('   - Create passcodes for this lock');
      console.log('   - Send ekeys to other users');
      console.log('   - Control the lock via Gateway APIs');
    }

  } catch (error) {
    console.error('❌ FAILED! Lock initialization error');
    console.error('');
    if (error.response) {
      console.error('Response data:', error.response.data);
      console.error('Response status:', error.response.status);
      console.error('');

      // Check for common errors
      if (error.response.data.errcode === -3001) {
        console.error('ℹ️  Error -3001: Invalid lockData');
        console.error('   This is expected if using placeholder lockData.');
        console.error('   Real lockData must come from TTLock SDK.addLock() method.');
      } else if (error.response.data.errcode === -2005) {
        console.error('ℹ️  Error -2005: Lock already initialized');
        console.error('   This lock has already been added to the system.');
      } else if (error.response.data.errcode === -1) {
        console.error('ℹ️  Error -1: Invalid access token');
      }
    } else {
      console.error('Error message:', error.message);
    }
  }
}

// Run the test
testLockInitialize();
