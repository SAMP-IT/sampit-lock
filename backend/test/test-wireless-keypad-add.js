async function testAddWirelessKeypad() {
  try {
    // Step 1: Get access token
    console.log('Step 1: Getting access token...');
    const hashedPassword = md5(password).toLowerCase();

    const tokenResponse = await axios.post(
      `${TTLOCK_API_BASE_URL}/oauth2/token`,
      null,
      {
        params: {
          client_id: TTLOCK_CLIENT_ID,
          client_secret: TTLOCK_CLIENT_SECRET,
          username: username,
          password: hashedPassword,
          grant_type: 'password'
        }
      }
    );

    const accessToken = tokenResponse.data.access_token;

    if (!accessToken) {
      console.error('❌ No access token received');
      console.error('OAuth Response:', tokenResponse.data);
      return;
    }

    console.log('✅ Access token obtained');
    console.log('   Token:', accessToken.substring(0, 20) + '...');
    console.log('');

    // Step 2: Add wireless keypad
    console.log('Step 2: Adding wireless keypad...');
    console.log('');

    const params = {
      clientId: TTLOCK_CLIENT_ID,
      accessToken: accessToken,
      lockId: parseInt(lockId),
      wirelessKeypadNumber: keypadNumber,
      wirelessKeypadName: keypadName,
      wirelessKeypadMac: keypadMac,
      wirelessKeypadFeatureValue: keypadFeatureValue,
      date: Date.now()
    };

    console.log('📡 Calling TTLock Add Wireless Keypad API...');
    console.log('Endpoint:', `${TTLOCK_API_BASE_URL}/v3/wirelessKeypad/add`);
    console.log('');

    const response = await axios.post(
      `${TTLOCK_API_BASE_URL}/v3/wirelessKeypad/add`,
      null,
      { params }
    );

    // Check for errors in response
    if (response.data.errcode && response.data.errcode !== 0) {
      console.error('❌ Add wireless keypad failed');
      console.error('');
      console.error('📊 Response:');
      console.error(JSON.stringify(response.data, null, 2));
      console.error('');

      if (response.data.errcode === -1) {
        console.error('ℹ️  Error -1: Invalid access token');
        console.error('   Please check your credentials.');
      } else if (response.data.errcode === -3) {
        console.error('ℹ️  Error -3: Invalid parameter');
        console.error('   Keypad number, MAC, or feature value is invalid.');
        console.error('   These values must come from TTLock SDK.');
      } else if (response.data.errcode === -2003) {
        console.error('ℹ️  Error -2003: Lock not found');
        console.error('   The lock ID does not exist or you do not have access to it.');
      } else if (response.data.errcode === -2018) {
        console.error('ℹ️  Error -2018: Permission Denied');
        console.error('   You do not have permission to add keypads to this lock.');
      } else if (response.data.errcode === 20002) {
        console.error('ℹ️  Error 20002: Not lock admin');
        console.error('   Only the lock admin can add wireless keypads.');
      }
      console.error('');
      console.error('To find valid lock IDs, run:');
      console.error('   node test-lock-list.js');
      console.error('');
      console.error('To get valid keypad parameters:');
      console.error('   - Use TTLock mobile SDK');
      console.error('   - Pair wireless keypad with lock');
      console.error('   - SDK will provide required parameters');
      return;
    }

    console.log('✅ SUCCESS! Wireless keypad added');
    console.log('');
    console.log('📊 Response:');
    console.log(JSON.stringify(response.data, null, 2));
    console.log('');

    const { wirelessKeypadId } = response.data;

    console.log('⌨️  WIRELESS KEYPAD ADDED:');
    console.log('   Wireless Keypad ID:', wirelessKeypadId);
    console.log('   Lock ID:', lockId);
    console.log('   Keypad Number:', keypadNumber);
    console.log('   Keypad Name:', keypadName);
    console.log('   Keypad MAC:', keypadMac);
    console.log('   Added At:', new Date().toISOString());
    console.log('');

    console.log('📱 NEXT STEPS:');
    console.log('   - Wireless keypad is now registered in cloud');
    console.log('   - Can be renamed via API:');
    console.log(`     node test-wireless-keypad-rename.js ${username} [password] ${wirelessKeypadId} "New Name"`);
    console.log('');
    console.log('   - Users can now use keypad to unlock:');
    console.log('     1. Enter valid passcode on keypad');
    console.log('     2. Keypad communicates with lock');
    console.log('     3. Lock verifies passcode');
    console.log('     4. Lock unlocks if passcode is valid');
    console.log('');

  } catch (error) {
    console.error('❌ FAILED! Add wireless keypad error');
    console.error('');
    if (error.response) {
      console.error('Response data:', error.response.data);
      console.error('Response status:', error.response.status);
      console.error('');

      // Check for common errors
      if (error.response.data.errcode === -1) {
        console.error('ℹ️  Error -1: Invalid access token');
        console.error('   Please check your credentials.');
      } else if (error.response.data.errcode === -3) {
        console.error('ℹ️  Error -3: Invalid parameter');
        console.error('   Keypad parameters must come from TTLock SDK.');
      }
    } else {
      console.error('Error message:', error.message);
    }
  }
}

// Run the test
testAddWirelessKeypad();
